_DATASET_DIRNAME = "BeefCattle_Muzzle_Individualized"

# Project root = folder containing this config.py.
PROJECT_ROOT = Path(__file__).resolve().parent


def _find_dataset_dir() -> Path:
    """Returns the first valid path that contains the dataset."""
    # 1. Environment override.
    env = os.environ.get("CATTLE_DATA_DIR")
    if env:
        return Path(env)

    candidates: list[Path] = []

    # 2. Kaggle: the dataset may be under any slug in /kaggle/input.
    kaggle_input = Path("/kaggle/input")
    if kaggle_input.is_dir():
        # direct match .../<slug>/BeefCattle_Muzzle_Individualized
        candidates += list(kaggle_input.glob(f"*/{_DATASET_DIRNAME}"))
        # in case the slug IS the dataset
        candidates += list(kaggle_input.glob(f"*/*/{_DATASET_DIRNAME}"))
        # recursive fallback: mount may be deeper, e.g.
        # /kaggle/input/datasets/<user>/<slug>/BeefCattle_Muzzle_Individualized
        deep = next(kaggle_input.rglob(_DATASET_DIRNAME), None)
        if deep is not None:
            candidates.append(deep)

    # 3. Local: data_local/ at the project root or in parent repos (worktree).
    for base in [PROJECT_ROOT, *PROJECT_ROOT.parents]:
        candidates.append(base / "data_local" / _DATASET_DIRNAME)
        candidates.append(base / "data" / _DATASET_DIRNAME)

    for c in candidates:
        if c.is_dir():
            return c

    # Fallback: return the expected local path even if it doesn't exist yet,
    # so the error message points to something concrete.
    return PROJECT_ROOT / "data_local" / _DATASET_DIRNAME


