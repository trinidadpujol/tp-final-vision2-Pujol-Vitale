def _find_pretrained_dir() -> Path | None:
    """Folder containing the pretrained .pth files, or None (→ downloaded on demand).

    Order: env CATTLE_PRETRAINED_DIR → /kaggle/input/*/ (where the .pth files are) →
    local pretrained_weights/ (in the project or parent repos / worktree).
    """
    env = os.environ.get("CATTLE_PRETRAINED_DIR")
    if env:
        return Path(env)

    any_file = next(iter(PRETRAINED_FILES.values()))
    candidates: list[Path] = []

    kaggle_input = Path("/kaggle/input")
    if kaggle_input.is_dir():
        # Kaggle folder containing the .pth (at any reasonable depth)
        candidates += [p.parent for p in kaggle_input.glob(f"*/{any_file}")]
        candidates += [p.parent for p in kaggle_input.glob(f"*/*/{any_file}")]
        # recursive fallback: mount may be deeper
        # (/kaggle/input/datasets/<user>/<slug>/...).
        deep = next(kaggle_input.rglob(any_file), None)
        if deep is not None:
            candidates.append(deep.parent)

    for base in [PROJECT_ROOT, *PROJECT_ROOT.parents]:
        candidates.append(base / "pretrained_weights")
        # ...or any immediate subfolder containing the .pth files (detected by
        # FILENAME, same as Kaggle): also works with imagenet-pretrained/.
        candidates += [p.parent for p in base.glob(f"*/{any_file}")]

    for c in candidates:
        if (c / any_file).is_file():
            return c
    return None


